create table public.exercises (
  id bigint generated by default as identity not null,
  name text not null,
  target_muscles muscles[] not null,
  required_equipment equipment[] null,
  emphasis public.emphasis_type not null,
  difficulty public.difficulty_type not null,
  tags text[] not null,
  instructions text[] not null,
  default_tracking_type tracking_type[] not null default array['reps'::tracking_type],
  video_url text null,
  source public.source not null default 'default'::source,
  updated_at timestamp with time zone null,
  created_at timestamp with time zone not null default now(),
  default_leverage_id bigint null,
  default_assist_id bigint null,
  constraint exercises_pkey primary key (id),
  constraint exercises_default_assist_id_fkey foreign KEY (default_assist_id) references leverages_assists (id),
  constraint exercises_default_leverage_id_fkey foreign KEY (default_leverage_id) references leverages_assists (id)
) TABLESPACE pg_default;